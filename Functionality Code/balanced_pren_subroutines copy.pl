#Example 1: Balanced 
print( ("()()" =~ /(^([^()]*\((?>[^()]|(?2))*\)[^()]*)+$)/)[0]);
print("\n");

#Example 2: Balanced with Characters in Between
print( ("(Hello( there(.)(general))kenobi)" =~ /(^([^()]*\((?>[^()]|(?2))*\)[^()]*)+$)/)[0]); 
print("\n");

#Example 3: Unbalanced
print( (")))()()(()" =~ /(^([^()]*\((?>[^()]|(?2))*\)[^()]*)+$)/)[0]); 
print("\n");

#Example 4: Two Types
print( ("({}}})()" =~ /(^((\((?>[^(){}]|(?2))*\))*(\{(?>[^(){}]|(?2))*\})*)$)/)[0]);
print("\n");

print( ("(){}}}" =~ /(^((\((?>[^(){}]|(?2))*\))*(\{(?>[^(){}]|(?2))*\})*)$)/)[0]);
print("\n");

print( ("(){()(}"=~ /(^((\((?>[^(){}]|(?2))*\))*(\{(?>[^(){}]|(?2))*\})*)$)/)[0]);
print("\n");